package org.bcit.midterm.questions;

public class Advanced04WaterSprinkler {
  //Instance variables
  private double range;
  private double angle;

  //Constructor
  public Advanced04WaterSprinkler(double range, double angle) {
    this.range = range;
    this.angle = angle;
  }

  //Accessor method
  //Returns the value of the range instance variable
  public double getRange() {
    return range;
  }

  //Mutator method
  //Sets the range instance variable's value
  public void setRange(double range) {
    this.range = range;
  }

  //Accessor method
  //Returns the value of the angle instance variable
  public double getAngle() {
    return angle;
  }

  //Mutator method
  //Sets the angle instance variable's value
  public void setAngle(double angle) {
    this.angle = angle;
  }
}




